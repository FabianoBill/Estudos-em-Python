def funcao(parcela1, parcela2):
    """Função que soma as parcelas passadas por parâmetro

    :param parcela1: number
    :param parcela2: number
    :return: number
    """
    return parcela1 + parcela2


if __name__ == "__main__":      # teste da função
    print(__name__)
    print(funcao(3, 4))
